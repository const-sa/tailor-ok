using System;
using System.Drawing;
using System.Windows.Forms;
using DevExpress.XtraEditors;
using SewingSystem.Classes.Zatca;

namespace SewingSystem.Forms
{
    /// <summary>
    /// صفحة شرح داخل البرنامج: الربط مع هيئة الزكاة، مكان الفواتير والمرتجع
    /// والمشتريات، مصدر رقم الفاتورة، وتعليمات الاستخدام. واجهة DevExpress (RTL).
    /// </summary>
    public class XtraFormZatcaHelp : FormZatcaMaster
    {
        public XtraFormZatcaHelp()
        {
            Text = "دليل الاستخدام والربط مع الزكاة";
            RightToLeft = RightToLeft.Yes; RightToLeftLayout = true;
            StartPosition = FormStartPosition.CenterScreen;
            ClientSize = new Size(780, 704);
            MinimumSize = new Size(620, 520);
            Font = new Font("Tahoma", 10f);
            ZatcaUi.ApplyIcon(this);
            BtnSave.Visible = false;     // لا يوجد ما يُحفظ في صفحة الدليل
            BtnRefresh.Visible = false;

            var box = new MemoEdit
            {
                Dock = DockStyle.Fill,
                RightToLeft = RightToLeft.Yes,
                Text = HelpText.Replace("\n", "\r\n")
            };
            box.Properties.ReadOnly = true;
            box.Properties.ScrollBars = ScrollBars.Vertical;
            box.Properties.Appearance.Font = new Font("Tahoma", 10.5f, FontStyle.Bold);
            box.Properties.Appearance.Options.UseFont = true;
            box.Properties.WordWrap = true;

            var pad = new PanelControl { Dock = DockStyle.Fill };
            pad.BorderStyle = DevExpress.XtraEditors.Controls.BorderStyles.NoBorder;
            pad.Padding = new System.Windows.Forms.Padding(10);
            pad.Controls.Add(box);

            ContentPanel.Controls.Add(pad);
            ContentPanel.Controls.Add(ZatcaUi.Header("دليل الاستخدام والربط مع الزكاة",
                                        "الفوترة الإلكترونية — المرحلة الثانية"));
        }

        public const string HelpText = @"
دليل استخدام البرنامج مع الفوترة الإلكترونية (هيئة الزكاة والضريبة والجمارك)
=====================================================================

أولاً: الربط مع الزكاة (المرحلة الثانية)
----------------------------------------
1. افتح من الشريط: «إعدادات ربط الزكاة».
2. أدخل بيانات المنشأة: الرقم الضريبي (15 رقماً)، الاسم النظامي، السجل التجاري،
   الرقم التسلسلي (EGS)، والعنوان الوطني، ثم اضغط «حفظ الإعدادات».
3. اضغط «توليد المفاتيح + CSR» (مرة واحدة).
4. من بوابة فاتورة احصل على رمز OTP والصقه في الحقل.
5. اضغط:
   - «تفعيل تجريبي (محاكاة)» للاختبار على بيئة المحاكاة، أو
   - «تفعيل مباشر (إنتاج)» للتشغيل الفعلي.
   ينفّذ البرنامج تلقائياً: شهادة الامتثال ← فحوصات الامتثال ← شهادة الإنتاج،
   ويظهر كل ذلك في «سجل الحالة».
6. بعد نجاح التفعيل: تُبلَّغ الفواتير للهيئة ويظهر رمز QR عليها.

ثانياً: أين صفحة الفواتير؟ ولماذا لا يمكن الحذف؟
------------------------------------------------
• فاتورة البيع تُنشأ وتُدار من شاشة «التفصيل» (شاشة الأصناف/التفصيل وإدارة التفاصيل).
• بعد طباعة/إصدار الفاتورة لا يمكن تعديلها أو حذفها (التزاماً بأنظمة الزكاة:
  الفاتورة المُصدَرة والمُبلَّغة لا تُحذف).
• التصحيح أو الاسترجاع يتم عبر «مرتجع مبيعات» (إشعار دائن) — وليس بالحذف.

ثالثاً: أين صفحات المرتجع؟
--------------------------
يوجد مساران:
1. «مرتجع مبيعات» (زر في الشريط) — الأنسب لإرجاع بضاعة:
   - تُدخل رقم الفاتورة الأصلية وتحمّلها.
   - تختار «مرتجع كامل» أو تعدّل الكميات لكل صنف (مرتجع جزئي).
   - يعكس الضريبة، يُرجع المخزون، ويُنشئ إشعاراً دائناً (نوع 381) مرتبطاً بالأصلية.
   - يمكن «إبلاغ الزكاة الآن» إن كان النظام مفعّلاً.
2. «الإشعارات الدائنة/المدينة» — للتسويات المالية بالمبلغ (دائن/مدين) على الفاتورة.

رابعاً: أين المشتريات؟
----------------------
• فواتير الشراء تُدار من شاشة «فاتورة المشتريات/الشراء».
• لها ترقيم مستقل خاص بها (لا يخلط مع ترقيم فواتير البيع).

خامساً: من أين يأتي رقم الفاتورة؟
--------------------------------
• رقم فاتورة البيع يُولَّد تلقائياً عند حفظ الفاتورة من شاشة «التفصيل»:
  الرقم = أكبر رقم فاتورة موجود + 1 (لكل فرع على حدة). فهو من مسار التفصيل وليس مساراً آخر.
• فاتورة المشتريات لها تسلسل أرقام منفصل خاص بها.
• المرتجع يأخذ رقماً جديداً من نفس تسلسل المبيعات، ويُحفظ مرتبطاً برقم الفاتورة الأصلية.

سادساً: سير العمل اليومي مع الزكاة
----------------------------------
1. أنشئ الفاتورة من شاشة التفصيل → احفظها (يتولّد الرقم تلقائياً).
2. اطبع الفاتورة → بعد الطباعة تُقفل من التعديل/الحذف، ويظهر عليها QR.
3. (عند التفعيل) تُبلَّغ الفاتورة للهيئة تلقائياً/يدوياً عبر «إبلاغ فاتورة برقمها».
4. لإرجاع بضاعة أو تصحيح مبلغ: استخدم «مرتجع مبيعات» (لا تحذف الفاتورة).
5. راجع حالة الإبلاغ (REPORTED/CLEARED) في بيانات الفاتورة.

ملاحظات مهمة
------------
• البيانات الحالية للمنشأة افتراضية — استبدلها ببياناتك الحقيقية قبل التشغيل المباشر.
• التفعيل يتطلب اتصال إنترنت ورمز OTP من بوابة فاتورة.
• احتفظ بنسخة احتياطية؛ المفاتيح والشهادات مخزّنة مشفّرة داخل النظام.
";
    }
}
